package com.nhnacademy.parking;

public class ParkTime {
    private int day;
    private int hour;
    private int min;
    private int sec;

    public ParkTime(int day, int hour, int min, int sec) {
        this.day = day;
        this.hour = hour;
        this.min = min;
        this.sec = sec;
    }
}
